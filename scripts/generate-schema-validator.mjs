#!/usr/bin/env node
import fs from "node:fs";
import crypto from "node:crypto";
import path from "node:path";
import Ajv2020 from "ajv/dist/2020.js";
import standaloneCode from "ajv/dist/standalone/index.js";

const schemaPath = path.resolve(".looppilot/core/decision-schema.json");
const outputPath = path.resolve("scripts/generated/decision-schema-validator.mjs");
const checkOnly = process.argv.includes("--check");
const schema = JSON.parse(fs.readFileSync(schemaPath, "utf8"));
const stable = (value) => {
  if (Array.isArray(value)) return value.map(stable);
  if (value && typeof value === "object") {
    return Object.fromEntries(Object.keys(value).sort().map((key) => [key, stable(value[key])]));
  }
  return value;
};
const schemaSha256 = crypto.createHash("sha256").update(JSON.stringify(stable(schema))).digest("hex");
const ajv = new Ajv2020({ allErrors: true, strict: false, code: { source: true, esm: true, lines: true } });
const validate = ajv.compile(schema);

let generated = standaloneCode(ajv, validate)
  .replace(
    /const (func\d+) = require\("ajv\/dist\/runtime\/equal"\)\.default;/,
    `const $1 = function deepEqual(left, right) {
  if (left === right) return true;
  if (!left || !right || typeof left !== "object" || typeof right !== "object") {
    return left !== left && right !== right;
  }
  if (left.constructor !== right.constructor) return false;
  if (Array.isArray(left)) {
    if (left.length !== right.length) return false;
    return left.every((entry, index) => deepEqual(entry, right[index]));
  }
  if (left.constructor === RegExp) return left.source === right.source && left.flags === right.flags;
  if (left.valueOf !== Object.prototype.valueOf) return left.valueOf() === right.valueOf();
  if (left.toString !== Object.prototype.toString) return left.toString() === right.toString();
  const keys = Object.keys(left);
  if (keys.length !== Object.keys(right).length) return false;
  return keys.every((key) => Object.prototype.hasOwnProperty.call(right, key) && deepEqual(left[key], right[key]));
};`,
  )
  .replace(
    /const (func\d+) = require\("ajv\/dist\/runtime\/ucs2length"\)\.default;/,
    "const $1 = (value) => [...value].length;",
  );

if (/require\(|from ["']ajv\//.test(generated)) {
  throw new Error("Generated validator still depends on the Ajv runtime.");
}

generated = `// Generated by scripts/generate-schema-validator.mjs. Do not edit.\nexport const decisionSchemaSha256 = "${schemaSha256}";\n${generated}`;

if (checkOnly) {
  const current = fs.existsSync(outputPath) ? fs.readFileSync(outputPath, "utf8") : "";
  if (current !== generated) {
    console.error("Generated decision validator is stale. Run npm run generate:schema-validator.");
    process.exit(1);
  }
  console.log("Generated decision validator is current and self-contained.");
} else {
  fs.mkdirSync(path.dirname(outputPath), { recursive: true });
  fs.writeFileSync(outputPath, generated, "utf8");
  console.log(`Generated ${path.relative(process.cwd(), outputPath)}.`);
}
